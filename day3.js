//PART 2 - checking multiple slopes
function mapChecker(arr) {
  let slopes = [
    [1, 1],
    [3, 1],
    [5, 1],
    [7, 1],
    [1, 2],
  ];

  let numberOfSlopes = slopes.length;

  let answer = 1;

  for (let i = 0; i < numberOfSlopes; i++) {
    let treeCount = 0;
    let row = 0;
    let column = 0;
    let numberOfRows = arr.length;
    let numberOfColumns = arr[0].length;

    while (row < numberOfRows) {
      let str = arr[row];
      if (str.charAt(column) == "#") {
        treeCount += 1;
      }

      if (column + slopes[i][0] < numberOfColumns) {
        column = column + slopes[i][0];
      } else {
        column = column + slopes[i][0] - numberOfColumns;
      }

      row += slopes[i][1];
    }

    answer = answer * treeCount;
  }

  console.log(answer);
}

